import SwiftUI

struct CategoryHome: View {
    @EnvironmentObject var modelData: ModelData
    
    var body: some View {
        NavigationView {
            List {
                ForEach(modelData.categories.keys.sorted(), id:\.self) { category in
                    Text(category)
                }
            }
            .navigationTitle("Featured")
        }
    }
}

